// Plugin to resolve @mastra/core subpaths
const mastraResolvePlugin = {
	name: 'mastra-resolve',
	setup(build) {
		build.onResolve({ filter: /^@mastra\/core\// }, args => {
			try {
				// Use Node's resolution to find the module
				const resolved = require.resolve(args.path);
				// Convert .cjs to .js for ESM compatibility
				const jsPath = resolved.replace(/\.cjs$/, '.js');
				if (fs.existsSync(jsPath)) {
					return { path: jsPath };
				}
				return { path: resolved };
			} catch (e) {
				console.error(`Failed to resolve ${args.path}:`, e.message);
				return undefined;
			}
		});
	},
};

// Plugin to shim Node.js builtins for @mlc-ai/web-llm (browser-only package)
// WebLLM's tvmjs tries to require("fs") etc. for Node.js code paths,
// but in Obsidian's Electron renderer we need it to use fetch() instead.
const webllmNodeShimPlugin = {
	name: 'webllm-node-shim',
	setup(build) {
		// List of Node.js builtins that webllm may try to import
		const shimmedModules = ['fs', 'path', 'url', 'module', 'crypto', 'perf_hooks', 'os', 'worker_threads'];

		// Only intercept requires from within @mlc-ai packages
		for (const mod of shimmedModules) {
			build.onResolve({ filter: new RegExp(`^${mod}$`) }, args => {
				// Check if the importer is from @mlc-ai/web-llm
				if (args.importer && args.importer.includes('@mlc-ai')) {
					return { path: mod, namespace: 'webllm-node-shim' };
				}
				// Let other importers use the real module (external)
				return undefined;
			});
		}

		// Provide empty shim content for intercepted modules
		build.onLoad({ filter: /.*/, namespace: 'webllm-node-shim' }, args => {
			const shims = {
				fs: `export default {}; export const readFileSync = undefined; export const writeFileSync = undefined; export const existsSync = () => false; export const mkdirSync = undefined; export const readdirSync = () => [];`,
				path: `export default {}; export const join = (...args) => args.join('/'); export const resolve = (...args) => args.join('/'); export const dirname = (p) => p; export const basename = (p) => p; export const extname = () => '';`,
				url: `export default {}; export const fileURLToPath = (u) => u; export const pathToFileURL = (p) => p;`,
				module: `export default {}; export const createRequire = () => () => ({});`,
				crypto: `export default {};`,
				perf_hooks: `export const performance = globalThis.performance || {}; export default { performance };`,
				os: `export default {}; export const cpus = () => []; export const platform = () => 'browser';`,
				worker_threads: `export default {}; export const isMainThread = true; export const parentPort = null;`,
			};
			return {
				contents: shims[args.path] || 'export default {};',
				loader: 'js',
			};
		});
	},
};

