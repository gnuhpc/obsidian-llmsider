// Copy plugin files to vaults
function copyToVaults() {
	const vaults = [
		path.join(process.env.HOME, "Documents/test-note/.obsidian/plugins/obsidian-llmsider"),
		path.join(process.env.HOME, "Documents/my-note/.obsidian/plugins/obsidian-llmsider"),
	];

	const filesToCopy = [
		{ name: "main.js", source: path.join(__dirname, "main.js") },
		{ name: "manifest.json", source: path.join(__dirname, "manifest.json") },
		{ name: "styles.css", source: path.join(__dirname, "styles.css") },
	];

	vaults.forEach((vaultPath) => {
		try {
			// Create directory if it doesn't exist
			if (!fs.existsSync(vaultPath)) {
				fs.mkdirSync(vaultPath, { recursive: true });
			}

			// Copy files
			filesToCopy.forEach(({ name, source }) => {
				if (fs.existsSync(source)) {
					fs.copyFileSync(source, path.join(vaultPath, name));
					console.log(`✓ Copied ${name} to ${vaultPath}`);
				}
			});

			console.log(`✓ Plugin files copied to ${vaultPath} successfully`);
		} catch (error) {
			// Ignore errors as requested
			console.log(`ℹ Could not copy to ${vaultPath} (this is non-critical):`, error.message);
		}
	});
}

